# For statement examples in Python

# Example 1: Iterate through a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example 2: Iterate through a range
for i in range(5):
    print(f"Number: {i}")

# Example 3: Iterate through a string
for char in "Python":
    print(char)

# Example 4: Iterate with index using enumerate
for index, fruit in enumerate(fruits):
    print(f"{index}: {fruit}")

# Example 5: Nested for loop
for i in range(3):
    for j in range(2):
        print(f"i={i}, j={j}")